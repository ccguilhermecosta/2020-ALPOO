    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String driver = jTxtDriver.getText();
        String banco = jTxtBanco.getText();
        String usuario = jTxtUsuario.getText();
        String senha = jPasswd.getText();
        String sql = jAreaSQL.getText();

        crudJDBC.conectar(driver, banco, usuario, senha);

        switch (operacao) {
            case "consultar":
                jTxtResultado.setText(crudJDBC.consultar(sql));
                break;
            case "incluir":
                crudJDBC.incluir(sql);
                break;
            case "alterar":
                crudJDBC.alterar(sql);
                break;
            case "excluir":
                crudJDBC.excluir(sql);
                break;
            default:
                JOptionPane.showMessageDialog(null, "Operação Inválida: " + operacao);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

