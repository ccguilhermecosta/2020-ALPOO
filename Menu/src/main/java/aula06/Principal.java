    private void configurarBanco() {
        switch (jComboSGBD.getSelectedItem().toString()) {
            case "PostgreSQL":
                jTxtDriver.setText("org.postgresql.Driver");
                jTxtBanco.setText("jdbc:postgresql://localhost/teste");
                jTxtUsuario.setText("teste");
                jPasswd.setText("teste");
                break;
            case "Oracle":
                jTxtDriver.setText("oracle.jdbc.OracleDriver");
                jTxtBanco.setText("jdbc:oracle:thin:@10.10.10.66:1521/pdb02");
                jTxtUsuario.setText("guilherme");
                jPasswd.setText("123");
                break;
            case "MySQL":
                jTxtDriver.setText("com.mysql.jdbc.Driver");
                jTxtBanco.setText("jdbc:mysql://localhost:3306/test");
                jTxtUsuario.setText("root");
                jPasswd.setText("root");
                break;
            default:
                JOptionPane.showMessageDialog(null,
                        "Banco de dados não possui driver JDBC instalado\n"
                        + "Confira no arquivo pom.xml");

        }
    }
